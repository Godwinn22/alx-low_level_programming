#include "3-calc.h"

/**
 * op_add - a function that returns the sum of two numbers
 * @a: the first number
 * @b: the second number
 * Return: returns the sum of the two numbers (a, b)
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that returns the difference of two numbers
 * @a: the first number
 * @b: the second number
 * Return: returns the subtraction of the two numbers (a, b)
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - a function that returns the multiplication of two numbers
 * @a: the first number
 * @b: the second number
 * Return: returns the multiplication of the two numbers (a, b)
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that returns the division of two numbers
 * @a: the first number
 * @b: the second number
 * Return: returns the division of the two numbers (a, b)
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that returns the modulus of two numbers
 * @a: the first number
 * @b: the second number
 * Return: returns the modulus of the two numbers (a, b)
*/
int op_mod(int a, int b)
{
	return (a % b);
}
